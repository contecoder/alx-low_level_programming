#include<string.h>
#include<ctype.h>
/**
 *string_toupper- check the
 *@s: the first
 *
 *
 *Return: Always 0.
 */
char *string_toupper(char *s)
{
char myupper[100];
int i;
for (i = 0 ; i <= 100; i++)
{
if (s[i] == '\0')
{
break;
}
else
{
myupper[i] = toupper(s[i]);
*(s + i) = myupper[i];
}
}
return (s);
}
