void print_alphabet_x10(void);
void print_alphabet_x10(void)
{
int i;
int j;
char alphabet[26] = "abcdefghijklmnopqrstuvwxyz";
for (j = 1 ; j <= 10 ; j++)
{
for (i = 0 ; i <= 25 ; i++)
{
putchar(alphabet[i]);
}
putchar('\n');
}
}
